CREATE TABLE ITEMS
(
    ID        BIGINT AUTO_INCREMENT PRIMARY KEY,
    NAME      VARCHAR(255) NOT NULL,
    PRICE     INT          NOT NULL CHECK (PRICE >= 0),
    IMAGE_URL VARCHAR(255)
);

CREATE TABLE USERS
(
    ID        BIGINT AUTO_INCREMENT PRIMARY KEY,
    EMAIL     VARCHAR(255) NOT NULL,
    PASSWORD  VARCHAR(255)
);
